#include<stdio.h>

int main(void){
    int ch = getchar();
    while (ch != EOF){
        if (ch >= '0' && ch <='9'){
            putchar(ch);
        } else if (ch != ' ' && ch != '\n'){
            ch = '.';
            putchar(ch);
        } else {
            putchar(ch);
        }
        ch = getchar();
    }
    return 0;
}
